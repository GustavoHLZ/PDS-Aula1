package Model;

public class Calculo {

	public Integer CalculoSoma (Integer num, Integer num1) {
		Integer resultado = num + num1;
		return resultado;
	}
	
	public Integer CalculoSubtracao (Integer num, Integer num1) {
		Integer resultado = num - num1;
		return resultado;
		
	}
	
	public Integer CalculoMultiplicacao (Integer num, Integer num1 ) {
		Integer resultado = num * num1;
		return resultado;
	}
	public Integer CalculoDivisao (Integer num, Integer num1) {
		Integer resultado = num / num1;
		return resultado;
	}
}
	
